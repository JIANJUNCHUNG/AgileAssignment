    private void jbtSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtSearchActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jtbProd.getModel();
         ArrayList<deleteProduct.product> list = productList();
         
         for(int i=0;i<list.size();i++){
             
         if(jtfProdID.getText().equals(list.get(i).ProdID.toString())){
             jtfProdName.setText(list.get(i).ProdName);
             jtfProdID.setText(list.get(i).ProdID);
             jtfProdType.setText(list.get(i).ProdType);
             jtfPrice.setText(Double.toString(list.get(i).Price));
             }
         }
    }//GEN-LAST:event_jbtSearchActionPerformed

    private void jbtDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtDeleteActionPerformed
        // TODO add your handling code here:
         DefaultTableModel model = (DefaultTableModel) jtbProd.getModel();
         ArrayList<deleteProduct.product> list = productList();
         
         int y= JOptionPane.showConfirmDialog(null, "Are you sure you want to delete?","Delete",JOptionPane.YES_NO_OPTION);
         if(y == JOptionPane.YES_OPTION){
             
         for(int i=0;i<list.size();i++){    
         if(jtfProdID.getText().equals(list.get(i).ProdID.toString())){
             model.removeRow(i);
             jtfProdID.setText("");
              jtfProdName.setText("");
               jtfProdType.setText("");
                jtfPrice.setText("");
         }
      }
         }else if( y == JOptionPane.NO_OPTION){
             jtfProdID.setText("");
              jtfProdName.setText("");
               jtfProdType.setText("");
                jtfPrice.setText("");
         }
   
  
       
         
    }//GEN-LAST:event_jbtDeleteActionPerformed

