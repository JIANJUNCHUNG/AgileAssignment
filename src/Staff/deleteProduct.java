    public class product{
        public String StaffID;
        public String ProdID;
        public String ProdName;
        public String ProdType;
        public double Price;
        
        public product(String StaffID, String ProdID, String ProdName, String ProdType, double Price){
            this.StaffID = StaffID;
            this.ProdID = ProdID;
            this.ProdName =  ProdName;
            this.ProdType = ProdType;
            this.Price = Price;
        }
    }
    
    public ArrayList productList(){
        
        ArrayList<product> list = new ArrayList<product>();
        product p1 = new product("S001","P001","Sun flower","Fresh Flower",12.00 );
        product p2 = new product("S002","P002","Rose","Bouqets",15.00 );
        product p3 = new product("S003","P003","Tulip","Floral arrangements",20.00 );
        list.add(p1);
        list.add(p2);
        list.add(p3);
        return list;
    }
    
    public void addRowtoTable(){
        DefaultTableModel model = (DefaultTableModel) jtbProd.getModel();
         ArrayList<deleteProduct.product> list = productList();
         Object rowData[] = new Object[5];
         for(int i=0;i<list.size();i++){
             rowData[0] =list.get(i).StaffID;
              rowData[1] =list.get(i).ProdID;
               rowData[2] =list.get(i).ProdName;
                rowData[3] =list.get(i).ProdType;
                 rowData[4] =list.get(i).Price;
                 
                 model.addRow(rowData);
                 
         }
        
    }
    

